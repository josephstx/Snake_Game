std::vector<std::string> Font::mtxText(std::string text) {
    std::vector<std::string> mtxLetter = {"", "", "", "", ""};

    for(int i = 0; i < 5; i++) {
        for(char c : text) {
            if(c != ' ') mtxLetter[i] += mtxLet[c][i] + " ";
            else mtxLetter[i] += "   ";
        }
    }

    return mtxLetter;
}